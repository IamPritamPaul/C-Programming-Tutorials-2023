// relational operators always return either true or false  (1 or 0)
#include <stdio.h>
int main()
{
    int a = 5, b = 6; // = is assignment oprator
    printf("a > b -> %d\n", a > b);
    printf("a >= b -> %d\n", a >= b);
    printf("a < b -> %d\n", a < b);
    printf("a <= b -> %d\n", a <= b);
    printf("a == b -> %d\n", a == b);
    printf("a != b -> %d\n", a != b);
    return 0;
}